#import "NSString+Size.h"

@implementation NSString (Size)

- (NSInteger)heightWithFont:(UIFont *)font maxWidth:(NSInteger)width
{
	NSMutableParagraphStyle *paragraphStyle = [[NSMutableParagraphStyle alloc] init];
	[paragraphStyle setLineBreakMode:NSLineBreakByWordWrapping];
	
	NSDictionary *attributes = @{ NSFontAttributeName:font, NSParagraphStyleAttributeName : paragraphStyle };
	
	CGRect rect = [self boundingRectWithSize:CGSizeMake(width, MAXFLOAT)
									 options:NSStringDrawingUsesLineFragmentOrigin
								  attributes:attributes
									 context:nil];
	
	return ceilf(rect.size.height);;
}

- (NSInteger)widthWithFont:(UIFont *)font maxHeight:(NSInteger)height
{
    NSMutableParagraphStyle *paragraphStyle = [[NSMutableParagraphStyle alloc] init];
    [paragraphStyle setLineBreakMode:NSLineBreakByWordWrapping];
    
    NSDictionary *attributes = @{ NSFontAttributeName:font, NSParagraphStyleAttributeName : paragraphStyle };
    
    CGRect rect = [self boundingRectWithSize:CGSizeMake(MAXFLOAT, height)
                                     options:NSStringDrawingUsesLineFragmentOrigin
                                  attributes:attributes
                                     context:nil];
    
    return ceilf(rect.size.width);;
}

// textView固定宽度下文字高度
- (NSInteger)textViewHeightWithFont:(UIFont *)font maxWidth:(NSInteger)width
{
    UITextView *textView = [[UITextView alloc] init];
    textView.font = font;
    textView.text = self;
    CGSize rect = [textView sizeThatFits:CGSizeMake(width, MAXFLOAT)];
    CGFloat height = rect.height + 1; // 计算误差
    return height;
}


+(NSString *)translation:(NSString *)arebic

{   NSString *str = arebic;
    NSArray *arabic_numerals = @[@"1",@"2",@"3",@"4",@"5",@"6",@"7",@"8",@"9",@"0"];
    NSArray *chinese_numerals = @[@"一",@"二",@"三",@"四",@"五",@"六",@"七",@"八",@"九",@"零"];
    NSArray *digits = @[@"个",@"十",@"百",@"千",@"万",@"十",@"百",@"千",@"亿",@"十",@"百",@"千",@"兆"];
    NSDictionary *dictionary = [NSDictionary dictionaryWithObjects:chinese_numerals forKeys:arabic_numerals];
    
    NSMutableArray *sums = [NSMutableArray array];
    for (int i = 0; i < str.length; i ++) {
        NSString *substr = [str substringWithRange:NSMakeRange(i, 1)];
        NSString *a = [dictionary objectForKey:substr];
        NSString *b = digits[str.length -i-1];
        NSString *sum = [a stringByAppendingString:b];
        if ([a isEqualToString:chinese_numerals[9]])
        {
            if([b isEqualToString:digits[4]] || [b isEqualToString:digits[8]])
            {
                sum = b;
                if ([[sums lastObject] isEqualToString:chinese_numerals[9]])
                {
                    [sums removeLastObject];
                }
            }else
            {
                sum = chinese_numerals[9];
            }
            
            if ([[sums lastObject] isEqualToString:sum])
            {
                continue;
            }
        }
        
        [sums addObject:sum];
    }
    
    NSString *sumStr = [sums  componentsJoinedByString:@""];
    NSString *chinese = [sumStr substringToIndex:sumStr.length-1];
    if (!([chinese rangeOfString:@"一十"].location == NSNotFound)) {
        chinese = [chinese substringFromIndex:1];
    }
    if ([[chinese substringToIndex:1] isEqualToString:@"零"]) {
        chinese = [chinese substringFromIndex:1];
    }
    
    return chinese;
}

